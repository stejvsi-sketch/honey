// Word-level swaps that create meaningfully different messages
const WORD_SWAPS = [
  // Contractions vs expanded
  ['cant', "can't"], ['dont', "don't"], ['didnt', "didn't"], ['wasnt', "wasn't"],
  ['isnt', "isn't"], ['wouldnt', "wouldn't"], ['shouldnt', "shouldn't"],
  ['couldnt', "couldn't"], ['hasnt', "hasn't"], ['havent', "haven't"],
  ['wont', "won't"], ['im', "i'm"], ['ive', "i've"], ['id', "i'd"],
  ['ill', "i'll"], ['thats', "that's"], ['hes', "he's"], ['shes', "she's"],
  ['weve', "we've"], ['theyre', "they're"], ['whats', "what's"],
  ['youre', "you're"], ['ure', "you're"],
  // Abbreviations
  ['because', 'bc'], ['before', 'b4'], ['tonight', '2nite'],
  ['really', 'rly'], ['probably', 'prolly'], ['something', 'smth'],
  ['someone', 'sb'], ['without', 'w/o'], ['between', 'btwn'],
  ['whatever', 'w/e'], ['forever', 'frvr'], ['together', 'tg'],
  ['honestly', 'honestly'], ['literally', 'literally'],
  ['remember', 'remember'], ['everything', 'everything'],
  // Emotion words
  ['miss', 'miss'], ['love', 'love'], ['hate', 'hate'],
  // Time words
  ['today', '2day'], ['tomorrow', 'tmrw'], ['tonight', '2night'],
  // Common words
  ['please', 'pls'], ['people', 'ppl'], ['about', 'abt'],
  ['right', 'rite'], ['night', 'nite'], ['with', 'w/'],
  ['though', 'tho'], ['through', 'thru'],
];

function applyCasing(msg) {
  const r = Math.random();
  if (r < 0.25) return msg;           // 25% keep as-is
  if (r < 0.60) return msg.toLowerCase(); // 35% lowercase (very common)
  if (r < 0.80) return msg.charAt(0).toUpperCase() + msg.slice(1); // 20% capitalize first
  if (r < 0.97) {
    return msg.replace(/(^|[.!?]\s+)([a-z])/g, (_, p, c) => p + c.toUpperCase()); // 17% sentence case
  }
  return msg.toUpperCase(); // 3% ALL CAPS
}

function applyPunctuation(msg) {
  return msg;
}

function applyYouSwap(msg) {
  const r = Math.random();
  if (r < 0.40) return msg; // 40% keep as-is
  if (r < 0.70) {
    return msg.replace(/\bu\b/g, 'you').replace(/\bur\b/g, 'your');
  }
  return msg.replace(/\byou\b/gi, 'u').replace(/\byour\b/gi, 'ur');
}

function applyICase(msg) {
  if (Math.random() < 0.70) return msg;
  return Math.random() < 0.5
    ? msg.replace(/\bI\b/g, 'i')
    : msg.replace(/\bi\b/g, 'I');
}

// Apply 2-4 random word-level swaps (contraction toggles, abbreviations)
function applyWordSwaps(msg) {
  const numSwaps = Math.random() < 0.4 ? 2 : (Math.random() < 0.6 ? 3 : 4);
  const candidates = shuffle(WORD_SWAPS).slice(0, numSwaps);
  for (const [a, b] of candidates) {
    if (a === b) continue;
    // Randomly pick direction
    const regex1 = new RegExp(`\\b${a.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\b`, 'gi');
    const regex2 = new RegExp(`\\b${b.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\b`, 'gi');
    if (Math.random() < 0.5 && regex1.test(msg)) {
      msg = msg.replace(regex1, b);
    } else if (regex2.test(msg)) {
      msg = msg.replace(regex2, a);
    }
  }
  return msg;
}

function processMessage(raw) {
  let msg = raw;
  // Strip ALL trailing punctuation
  msg = msg.trim().replace(/[.!?,;:…]+$/, '').trim();
  msg = applyWordSwaps(msg);
  msg = applyYouSwap(msg);
  msg = applyCasing(msg);
  msg = applyPunctuation(msg);
  msg = applyICase(msg);
  // Final cleanup — strip any trailing punctuation that snuck back in
  msg = msg.replace(/\s+/g, ' ').trim().replace(/[.!?,;:…]+$/, '').trim();
  return msg;
}


