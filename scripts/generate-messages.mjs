function generate() {
  const pool = [];
  const seen = new Set();
  const nameUsage = {};
  const MAX_PER_NAME = 7;

  function pickName() {
    // Weighted pick from NAMES (USCANAU names repeated 3x = higher chance)
    // But cap each unique name at MAX_PER_NAME
    for (let attempt = 0; attempt < 50; attempt++) {
      const n = R(NAMES);
      if ((nameUsage[n] || 0) < MAX_PER_NAME) return n;
    }
    // Fallback: find any name under cap from full unique list
    const shuffled = shuffle(UNAMES);
    for (const n of shuffled) {
      if ((nameUsage[n] || 0) < MAX_PER_NAME) return n;
    }
    return R(UNAMES);
  }

  const sourceUsage = {}; // Track how many times each raw message is used

  function tryAdd(msg, sourceIdx) {
    if (!msg || msg.length < 1 || msg.length > 280) return false;
    const key = msg.toLowerCase().trim();
    if (seen.has(key)) return false;
    seen.add(key);
    const name = pickName();
    nameUsage[name] = (nameUsage[name] || 0) + 1;
    pool.push({ name, message: msg, color_id: R(CARD_COLORS) });
    if (sourceIdx !== undefined) {
      sourceUsage[sourceIdx] = (sourceUsage[sourceIdx] || 0) + 1;
    }
    return true;
  }

  // Phase 1: Every raw message used once, mostly as-is
  const indices1 = [...RAW_MESSAGES.keys()];
  const shuffled1 = shuffle(indices1);
  for (const idx of shuffled1) {
    tryAdd(processMessage(RAW_MESSAGES[idx]), idx);
  }
  console.log(`Phase 1: ${pool.length} from raw pool`);

  // Phase 2: Generate variants, max 12 per source
  const MAX_PER_SOURCE = 12;
  let attempts = 0;
  let lastReport = pool.length;
  while (pool.length < 10000 && attempts < 1200000) {
    attempts++;
    const idx = Math.floor(Math.random() * RAW_MESSAGES.length);
    if ((sourceUsage[idx] || 0) >= MAX_PER_SOURCE) continue;
    const msg = processMessage(RAW_MESSAGES[idx]);
    tryAdd(msg, idx);
    if (pool.length >= lastReport + 1000) {
      console.log(`  ${pool.length}...`);
      lastReport = pool.length;
    }
  }
  console.log(`Phase 2: ${pool.length} total (${attempts} attempts)`);

  return shuffle(pool);
}


